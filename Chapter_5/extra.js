function returnGroupID(array, name) {
    const result = array.find(object => object.name.toLowerCase() === name.toLowerCase());
    return result ? result.groupID : null;
}

function returnGroupMembers(array, name) {
    let groupID = returnGroupID(array,name);
    let output = array.filter(object => object.groupID === groupID).map(object => object.name);
    return output !== [] ? output : "There is no group for this name";
}

